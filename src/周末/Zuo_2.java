package 周末;

public class Zuo_2 {

	public static void main(String[] args) {
		int a =88;
		int b =a/10;
		switch(b){
		case 0-5: 			
			System.out.println("成绩a的等级：E");
			break;
		case 6:
			System.out.println("成绩a的等级：D");
			break;
		case 7:
			System.out.println("成绩a的等级：C");
			break;
		case 8:
			System.out.println("成绩a的等级：B");
			break;
		case 9-10:
			System.out.println("成绩a的等级：A");
			break;
		default:
			System.out.println("成绩a无效");
			break;
		}

	}

}
